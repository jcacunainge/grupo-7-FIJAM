function mostrarTextofijam(){
    document.getElementById('mision').style.display ='none';
    document.getElementById('vision').style.display ='none';
    document.getElementById('fijam').style.display ='block';
    document.getElementById('fijam--fijam').style.display ='block';
    document.getElementById('fijam--mision').style.display ='none';
    document.getElementById('fijam--vision').style.display ='none';
}
